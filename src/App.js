// Utility function to calculate average of an array
const average = (arr) =>
  arr.reduce((acc, cur, i, arr) => acc + cur / arr.length, 0);

// Use environment variable for API key
const KEY = process.env.REACT_APP_OMDB_API_KEY;

export default function App() {
  // State variables for query, movies, watched list, loading status, errors, and selected movie ID
  const [query, setQuery] = useState("");
  const [movies, setMovies] = useState([]);
  const [watched, setWatched] = useState([]);
  const [isLoading, setIsLoading] = useState(false);
  const [error, setError] = useState("");
  const [selectedId, setSelectedId] = useState(null);

  // Handle movie selection toggle
  function handleSelectMovie(id) {
    setSelectedId((selectedId) => (id === selectedId ? null : id));
  }

  // Close the selected movie details view
  function handleCloseMovie() {
    setSelectedId(null);
  }

  // Add a movie to the watched list
  function handleAddWatched(movie) {
    setWatched((watched) => [...watched, movie]);
  }

  // Remove a movie from the watched list
  function handleDeleteWatched(id) {
    setWatched((watched) => watched.filter((movie) => movie.imdbID !== id));
  }

  useEffect(
    function () {
      const controller = new AbortController(); // Create an AbortController to cancel fetch if needed

      async function fetchMovies() {
        try {
          setIsLoading(true); // Show loading spinner
          setError(""); // Clear previous error
          const res = await fetch(
            `https://www.omdbapi.com/?apikey=${KEY}&s=${query}`,
            { signal: controller.signal }
          );
          if (!res.ok)
            throw new Error("Something went wrong with fetching movies");

          const data = await res.json();
          if (data.Response === "False") throw new Error("Movie not Found");

          setMovies(data.Search); // Set fetched movies
        } catch (err) {
          if (err.name !== "AbortError") {
            setError(err.message); // Show error message if not aborted
          }
        } finally {
          setIsLoading(false); // Hide loading spinner
        }
      }

      if (query.length < 3) {
        setMovies([]); // Clear movies if query is too short
        setError("");
        return;
      }
      handleCloseMovie(); // Close movie details on new search
      fetchMovies();

      return function () {
        controller.abort(); // Abort fetch if component unmounts or query changes
      };
    },
    [query]
  );

  return (
    <>
      <NavBar>
        <Search query={query} setQuery={setQuery} />
        <Numresults movies={movies} />
      </NavBar>

      <Main>
        <Box>
          {isLoading && <Loader />} {/* Show loader while fetching */}
          {!isLoading && !error && (
            <MovieList movies={movies} onSelectMovie={handleSelectMovie} />
          )}
          {error && <ErrorMessage message={error} />}
        </Box>

        <Box>
          {selectedId ? (
            <MovieDetails
              selectedId={selectedId}
              onCloseMovie={handleCloseMovie}
              onAddWatched={handleAddWatched}
              watched={watched}
            />
          ) : (
            <>
              <WatchedSummary watched={watched} />
              <WatchedMoviesList
                watched={watched}
                onDeleteWatched={handleDeleteWatched}
              />
            </>
          )}
        </Box>
      </Main>
    </>
  );
}

